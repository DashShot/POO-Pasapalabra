    private void jButton23ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton23ActionPerformed
        if (almacenJugadores.getJugadores().size() == 0) {
            jTextArea1.setText("No has iniciado sesión, por tanto no puedes añadir a otro jugador");
        } else if (almacenJugadores.getJugadores().get(j1).isAdmin()) {
            log.setNombre(jTextField79.getText());
            log.setKey(jPasswordField3.getText());
            if (jTextField79.getText().length() == 0 || jPasswordField3.getText().length() == 0) {
                jTextArea1.setText("Tienes que introducir un nombre y contraseña para poder registrarte como jugador");
            } else {
                if (log.AnadirJugador(log.getNombre(), log.getKey()) == true) {
                    jTextArea1.setText("Jugador añadido");

                    almacenJugadores.getJugadores().get(almacenJugadores.buscarPosicion(jTextField79.getText(), jPasswordField3.getText())).actualizarPuntos();

                } else {
                    jTextArea1.setText("Juagdor no añadido");
                }
            }
        } else {
            jTextArea1.setText("Solo un Admin puede añadir a otro jugador");
        }

    }//GEN-LAST:event_jButton23ActionPerformed

