    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed

        log.setNombre(textField1.getText());
        log.setKey(jPasswordField1.getText());
        if (textField1.getText().length() == 0 || jPasswordField1.getText().length() == 0) {
            jTextArea1.setText("Tienes que introducir un nombre y contraseña para poder registrarte como jugador");
        } else if ((j1 > almacenJugadores.getJugadores().size() + 1) || almacenJugadores.getJugadores().get(j1).isAdmin()) {
            if (log.getNombre().equals("admin") && log.getKey().equals("admin")) {     //QUE SEA ADMIN
                log.AnadirAdmin(log.getNombre(), log.getKey());
                jTextArea1.setText("Admin añadido, sesión iniciada");
                j1 = almacenJugadores.buscarPosicion(log.getNombre(), log.getKey());
                almacenJugadores.getJugadores().get(j1).setAdmin(true);
                jTextField3.setText(almacenJugadores.getJugadores().get(j1).getNombre());
                almacenJugadores.getJugadores().get(j1).actualizarPuntos();
                jTextField65.setText(String.valueOf(almacenJugadores.getJugadores().get(j1).getE().getPuntos()));
            } else if (log.AnadirJugador(log.getNombre(), log.getKey()) == true) {
                jTextArea1.setText("Jugador añadido, sesión iniciada");
                j1 = almacenJugadores.buscarPosicion(log.getNombre(), log.getKey());
                jTextField3.setText(almacenJugadores.getJugadores().get(j1).getNombre());
                almacenJugadores.getJugadores().get(j1).actualizarPuntos();
                jTextField65.setText(String.valueOf(almacenJugadores.getJugadores().get(j1).getE().getPuntos()));
            } else {
                jTextArea1.setText("Juagdor no añadido");
            }
        } else {
            jTextArea1.setText("Solo los admins pueden registrar a otros jugadores");
        }


    }//GEN-LAST:event_jButton9ActionPerformed

